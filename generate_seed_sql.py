#!/usr/bin/env python3
"""
Generate SQL seed file from Excel waybill data
Extracts: Drivers, Customers, Deliveries, Waybills, Waybill Items
"""

import pandas as pd
import json
import sys
import re
from datetime import datetime
from collections import defaultdict

# Excel file path
EXCEL_FILE = 'waybill_excel/Waybills_31-10-2025.xlsx'
OUTPUT_FILE = 'assets/waybill_seed.sql'

# Table prefixes (will be replaced with actual prefix in SQL)
# Note: This will be used as {PREFIX}kit_tablename, so use just 'wp_' if your prefix is 'wp_kit_'
PREFIX = 'wp_'

def clean_string(value):
    """Clean string values, handle NaN and None"""
    if pd.isna(value) or value is None:
        return None
    return str(value).strip() if str(value).strip() else None

def clean_decimal(value):
    """Clean decimal values"""
    if pd.isna(value) or value is None:
        return 0.0
    if isinstance(value, str):
        # Remove non-numeric characters except decimal point
        cleaned = re.sub(r'[^0-9.]', '', value)
        return float(cleaned) if cleaned else 0.0
    return float(value)

def clean_int(value):
    """Clean integer values"""
    if pd.isna(value) or value is None:
        return 0
    if isinstance(value, str):
        cleaned = re.sub(r'[^0-9]', '', value)
        return int(cleaned) if cleaned else 0
    return int(value)

def escape_sql(value):
    """Escape SQL string values"""
    if value is None:
        return 'NULL'
    # Replace single quotes with doubled quotes
    return "'" + str(value).replace("'", "''") + "'"

def format_datetime(value):
    """Format datetime for SQL"""
    if pd.isna(value) or value is None:
        return 'CURRENT_TIMESTAMP'
    if isinstance(value, pd.Timestamp):
        return f"'{value.strftime('%Y-%m-%d %H:%M:%S')}'"
    if isinstance(value, str):
        # Try to parse the string
        try:
            dt = pd.to_datetime(value)
            return f"'{dt.strftime('%Y-%m-%d %H:%M:%S')}'"
        except:
            return 'CURRENT_TIMESTAMP'
    return 'CURRENT_TIMESTAMP'

def format_date(value):
    """Format date for SQL"""
    if pd.isna(value) or value is None:
        return 'NULL'
    if isinstance(value, pd.Timestamp):
        return f"'{value.strftime('%Y-%m-%d')}'"
    if isinstance(value, str):
        try:
            dt = pd.to_datetime(value)
            return f"'{dt.strftime('%Y-%m-%d')}'"
        except:
            return 'NULL'
    return 'NULL'

def parse_customer_name(customer_name):
    """Parse customer name into first name and surname"""
    if not customer_name or pd.isna(customer_name):
        return ('Unknown', 'Customer')
    
    parts = str(customer_name).strip().split(' ', 1)
    first_name = parts[0] if parts else 'Unknown'
    surname = parts[1] if len(parts) > 1 else 'Customer'
    return (first_name, surname)

def get_city_country_mapping():
    """
    Return mapping of city names to (city_id, country_id)
    Loads from exported city_mapping.json file (generated by export_city_mapping.php)
    Falls back to hardcoded mapping if file doesn't exist
    """
    import os
    
    # Try to load from exported JSON file first
    mapping_file = os.path.join(os.path.dirname(__file__), 'assets', 'city_mapping.json')
    if os.path.exists(mapping_file):
        try:
            with open(mapping_file, 'r', encoding='utf-8') as f:
                raw_mapping = json.load(f)
                # Convert to simplified format: city_name -> (city_id, country_id)
                mapping = {}
                for key, value in raw_mapping.items():
                    if isinstance(value, dict):
                        mapping[key] = (value['city_id'], value['country_id'])
                print(f"Loaded {len(mapping)} city mappings from {mapping_file}")
                return mapping
        except Exception as e:
            print(f"Warning: Failed to load city mapping from {mapping_file}: {e}")
            print("Falling back to hardcoded mapping...")
    
    # Fallback: hardcoded mapping (should not be needed if export script works)
    mapping = {}
    print("Warning: Using hardcoded city mapping. Run export_city_mapping.php for accurate IDs.")
    # South Africa (country_id likely 1)
    za_cities = ['Johannesburg', 'Cape Town', 'Durban', 'Pretoria', 'Port Elizabeth']
    for idx, city in enumerate(za_cities, start=1):
        mapping[city] = (idx, 1)
    
    # Tanzania (country_id likely 2)
    tz_cities = ['Dar es Salaam', 'Dodoma', 'Arusha', 'Mbeya', 'Mwanza', 'Iringa', 
                 'Kilombero', 'Mafinga', 'Makambako', 'Mfundi', 'Mikumi', 'Morogoro', 
                 'Moshi', 'Sao Hill', 'Sumbawanga', 'Tanga', 'Zanzibar', 'Gombe Kinshasa']
    start_id = len(za_cities) + 1
    for idx, city in enumerate(tz_cities, start=start_id):
        mapping[city] = (idx, 2)
    
    return mapping

def get_direction_id(origin_country_id, destination_country_id, city_map):
    """
    Determine direction_id from origin and destination countries
    direction_id 1: South Africa -> South Africa (warehouse)
    direction_id 2: South Africa -> Tanzania
    direction_id 3: Tanzania -> South Africa
    """
    # Load direction mapping from database or use defaults
    # Default mapping based on known structure:
    # SA (country_id=1) -> SA (country_id=1) = direction_id 1 (warehouse)
    # SA (country_id=1) -> TZ (country_id=2) = direction_id 2
    # TZ (country_id=2) -> SA (country_id=1) = direction_id 3
    
    if origin_country_id == 1 and destination_country_id == 1:
        return 1  # Warehouse (SA -> SA)
    elif origin_country_id == 1 and destination_country_id == 2:
        return 2  # SA -> TZ
    elif origin_country_id == 2 and destination_country_id == 1:
        return 3  # TZ -> SA
    else:
        # Default to SA -> TZ (direction_id 2) for unknown combinations
        return 2

